/*
  # Fix Function Search Path Mutable Warnings

  ## Summary
  Sets a fixed search_path on all custom public functions to resolve 278 security warnings
  from the Supabase Security Advisor ("Function Search Path Mutable").

  ## What this fixes
  When a function has a mutable search_path, a malicious user could potentially manipulate
  which schema objects the function resolves to. Setting search_path = '' forces all object
  references inside the function to use fully-qualified names, eliminating this attack vector.

  ## Approach
  ALTER FUNCTION ... SET search_path = '' is applied to every custom function in the public
  schema. This does NOT change function logic - only the search_path configuration.
*/

